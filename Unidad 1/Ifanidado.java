// Ejercicio para comparar dos números enteros

public class Ifanidado {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 7;
        if (num1 > num2){
            System.out.println("El número "+num1 +" es mayor al número " +num2);
        }
        else if (num1 < num2){
            System.out.println("El número "+num1 +" es menor al número " +num2);
        }
        else {
            System.out.println("Los números "+num1 +" y " +num2 +" son iguales");
        }
    }
}
