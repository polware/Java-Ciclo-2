public class Operacion_Booleanos {
    public static void main(String[] args) {  
        Boolean b1 = true;  
        // will return a boolean instance corresponding to Boolean b1  
        Boolean b2 = Boolean.valueOf(b1);  
        System.out.println("Método valueOf() de variable b2 retorna = "+b2);  
        Boolean b3 = Boolean.valueOf(false);  
        System.out.println("Método valueOf() de variable b3 retorna = "+b3);  
       }  
}
